// Operadores Aritméticos
// Soma (+)
console.log(2 + 3);

// Subtração (-)
console.log(3 - 2);

// Multiplicação (*)
console.log(10 * 2);

// Divisão (/)
console.log(10 / 2);

// Potência (**): 2 ** 3 // base = 2, expoente = 3
console.log(2 ** 3);

// mod (%): calcula o resto da divisão
// 3 % 2 = 1 => resto da divisão
console.log(3 % 2);

// Atribuição (=)
curso = 'Curso de Técnico em Informática';
console.log(curso); 